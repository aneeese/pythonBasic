#USING WHILE LOOP
num=int(input("Enter the number: "))
count=1
while count<11:
    multiply=num*count
    print(num,"*",count,"=",multiply)
    count=count+1
print("End")
#USING FOR LOOP
num=int(input("Enter the number: "))
for count in range(1,11):
    multiply=num*count
    print(num,"*",count,"=",multiply)
print("End")












































      








